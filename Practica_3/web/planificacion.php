        <h3>Reparto del trabajo</h3>
        <p>Debido a que dentro del grupo tenemos conocimientos similares sobre aplicaciones web, ninguno tiene preferencia sobre una tarea 
            concreta. Sin embargo, priorizamos completar primero las más sencillas de manera individual, para terminar luego más complicadas 
            entre parejas (a no ser que esté obstaculizando el desarrollo de otras tareas, en cuyo caso priorizamos la complicada).
        </p>
    </div>
    <div class="plazos-entrega">
        <h3>Plazos de entrega</h3>
        <p>Los plazos de entrega se discuten tras el reparto del trabajo. El objetivo es poder completar las tareas
            pocos días antes de la entrega oficial exigida. De esta manera, hay tiempo de correción de errores o posibles cambios que mejoren
            el proyecto. También se tendrá en cuenta la dependencia de la tarea sobre otras para evitar retrasos y permitir que se pueda 
            trabajar de manera paralela junto a otras tareas.
        </p>
    </div>
    <div class="medios-reunion">
        <h3>Medios de reunión</h3>
        <p>Para las reuniones presenciales, se usarán las horas de laboratorio programadas por la asignatura. Para el trabajo online, 
            se usarán Github, Whatssap y quizá Discord.
        </p>

        <p>Reuniones presenciales: donde se establecen el reparto del trabajo y sus plazos de entrega.
             
            Github: donde se localiza el repositorio de la aplicación web. También contiene una lista de las tareas ya asignadas.
             
            Whatssap: usado principalmente para realizar preguntas cortas, comunicar algún inconveniente o solicitar una reunión para tratar 
            un problema mayor.
        </p>
        
        <p>En el caso ideal, solo serán necesarias estos 3 medios de reunión, pero en caso de necesitar resolver un problema y no ser
            posible de manera presencial por su gravedad se realizaría una reunión por Discord, donde se trataría de dar una solución para 
            evitar el atasco de tareas o posibles retrasos.
        </p>
